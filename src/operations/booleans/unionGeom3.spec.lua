test("union of geom3 with rounding issues #137", function()
	local geometry1 = center({ relativeTo = { 0, 0, -1 } }, cuboid({ size = { 44, 26, 5 } }))
	local geometry2 = center({ relativeTo = { 0, 0, -4.400001 } }, cuboid({ size = { 44, 26, 1.8 } })) -- introduce precision error
	local obs = union(geometry1, geometry2)
	local pts = geom3.toPoints(obs)
	expect(function()
		return geom3.validate(obs)
	end)["not"].toThrow()
	expect(#pts).toBe(6) -- number of polygons in union
end)
